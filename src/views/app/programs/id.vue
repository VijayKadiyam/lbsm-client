<template>
  <div class="main-content">
    <breadcumb :page="'Update Program'" :folder="'Programs'" />

    <b-row class="justify-content-md-center">
      <b-col md="9">
        <b-card title="Update Program">
          <b-row>
            <b-col md="12">
              <b-button
                style="float: right; margin-top: -45px"
                @click="$router.back()"
                variant="primary"
                ><i class="i-Arrow-Back-3"></i> Back</b-button
              >
            </b-col>
          </b-row>
          <b-form @submit.prevent="submit">
            <b-form-group label="Program Name">
              <b-form-input
                class="mb-2"
                label="Program Name"
                placeholder="Enter Program Name"
                v-model.trim="$v.form.program_name.$model"
              >
              </b-form-input>
              <b-alert
                show
                variant="danger"
                class="error mt-1"
                v-if="!$v.form.program_name.required"
                >Field is required</b-alert
              >
            </b-form-group>

            <b-form-group label="Program Description">
              <b-form-textarea
                id="textarea"
                v-model="form.program_description"
                placeholder="Program Description"
                rows="3"
                max-rows="6"
              ></b-form-textarea>
            </b-form-group>

            <b-form-group label="Short Name">
              <b-form-input
                class="mb-2"
                label="Short Name"
                placeholder="Enter Short Name"
                v-model.trim="form.short_name"
              >
              </b-form-input>
            </b-form-group>
            <b-form-group label="Instructor">
              <b-form-input
                class="mb-2"
                label="Instructor"
                placeholder="Enter Instructor"
                v-model.trim="form.instructor"
              >
              </b-form-input>
            </b-form-group>

            <b-form-group label="Hours">
              <b-form-input
                class="mb-2"
                label="Hours"
                placeholder="Enter Hours"
                v-model="form.hours"
              >
              </b-form-input>
            </b-form-group>

            <b-form-group label="Active">
              <b-row>
                <b-col md="8">
                  <span>IN ACTIVE</span>
                  <label class="switch switch-success mr-3 ml-3">
                    <input
                      type="checkbox"
                      checked="checkbox"
                      v-model="form.active"
                    /><span class="slider"></span>
                  </label>
                  <span>ACTIVE</span>
                </b-col>
              </b-row>
            </b-form-group>

            <b-button
              type="submit"
              variant="primary"
              :disabled="submitStatus === 'PENDING'"
              >Submit</b-button
            >

            <p v-once class="typo__p" v-if="submitStatus === 'OK'">
              {{ makeToastTwo("success") }}
            </p>
            <p v-once class="typo__p" v-if="submitStatus === 'ERROR'">
              {{ makeToast("danger") }}
            </p>
            <div v-once class="typo__p" v-if="submitStatus === 'PENDING'">
              <div class="spinner sm spinner-primary mt-3"></div>
            </div>
          </b-form>
        </b-card>
      </b-col>
    </b-row>
  </div>
</template>

<script>
import axios from "axios";
import { required, minLength } from "vuelidate/lib/validators";
export default {
  metaInfo: {
    // if no subcomponents specify a metaInfo.title, this title will be used
    title: "Update Program",
  },
  data() {
    return {
      form: {
        program_name: "",
        program_description: "",
        intructor: "",
        hours: "",
      },

      submitStatus: null,
    };
  },
  validations: {
    form: {
      program_name: {
        required,
        minLength: minLength(4),
      },
    },
  },
  mounted() {
    this.getData();
  },
  methods: {
    async getData() {
      this.isLoading = true;
      let form = await axios.get(`/programs/${this.$route.params.id}`);
      this.form = form.data.data;
      this.isLoading = false;
    },
    //   validate form
    async submit() {
      console.log("submit!");

      this.$v.$touch();
      if (this.$v.$invalid) {
        this.submitStatus = "ERROR";
      } else {
        // do your submit logic here
        try {
          this.submitStatus = "PENDING";
          this.isLoading = true;
          await axios.patch(`/programs/${this.$route.params.id}`, this.form);
          this.submitStatus = "OK";
          // setTimeout(() => {
          this.$router.push("/app/programs/");
          // }, 1000);
          this.isLoading = false;
        } catch (e) {
          this.isLoading = false;
          this.submitStatus = "ERROR";
        }
      }
    },
    makeToast(variant = null) {
      this.$bvToast.toast("Please fill the form correctly.", {
        title: `Variant ${variant || "default"}`,
        variant: variant,
        solid: true,
      });
    },
    makeToastTwo(variant = null) {
      this.$bvToast.toast("Successfully Submitted", {
        title: `Variant ${variant || "default"}`,
        variant: variant,
        solid: true,
      });
    },

    inputSubmit() {
      console.log("submitted");
    },
  },
};
</script>
